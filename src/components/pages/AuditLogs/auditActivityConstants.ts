export function severityChipLabel(code: string): string {
  switch (code.toUpperCase()) {
    case 'INFO':
      return 'Info';
    case 'NOTICE':
      return 'Notice';
    case 'WARNING':
      return 'Warning';
    case 'CRITICAL':
      return 'Critical';
    default:
      return code;
  }
}

